class GulpQuery(BaseModel):
    """
    A query
    """

    def __init__(
        self,
        q: Any,
        name: str = None,
        sigma_id: str = None,
        tags: list[str] = None,
        external_plugin: str = None,
        external_plugin_params: GulpPluginParameters = None,
    ):
        if name is None:
            # autogenerate name
            name = "query_%s" % (muty.string.generate_unique())
        if tags is None:
            tags = []

        super().__init__(
            name=name,
            q=q,
            sigma_id=sigma_id,
            tags=tags,
            external_plugin=external_plugin,
            external_plugin_params=external_plugin_params,
        )

    model_config = ConfigDict(
        json_schema_extra={
            "examples": [
                {
                    "name": "test",
                    "id": "test",
                    "q": {"query": {"match_all": {}}},
                    "tags": ["test"],
                    "external_plugin": "test_plugin",
                    "plugin_params": {
                        "custom": "parameter",
                    },
                }
            ]
        }
    )
    q: Any = Field(..., description="the query, usually a dict.")
    name: Optional[str] = Field(
        None,
        description="the name/title of the query (autogenerated if None).",
    )
    sigma_id: Optional[str] = Field(
        None, description="the id of the sigma rule, if this is a sigma query."
    )
    tags: Optional[list[str]] = Field([], description="query tags.")
    external_plugin: Optional[str] = Field(
        None, description="the external plugin to use, if this is an external query."
    )
    external_plugin_params: Optional[GulpPluginParameters] = Field(
        None,
        description="custom external plugin parameters, if this is an external query.",
    )


